public class VariabelExample {
    public static void main(String[] args) {
        // Deklarasi variabel
        int umur;

        // Inisialisasi variabel
        umur = 17;
        System.out.println("Umur awal: " + umur);

        // Update nilai variabel
        umur = 18;
        System.out.println("Umur setelah update: " + umur);

        // Contoh variabel lain
        String nama = "Fuad";
        System.out.println("Nama saya: " + nama);

        // Update variabel nama
        nama = "Fuad Adnansyah";
        System.out.println("Nama lengkap saya: " + nama);
    }
}
