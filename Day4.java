public class EscapeSequenceExample {
    public static void main(String[] args) {
        // \n = pindah baris
        System.out.println("Nama saya Fuad Adnansyah.\nSaya sedang belajar Java.");

        // \t = tab (spasi lebar)
        System.out.println("Nama:\tFuad Adnansyah");
        System.out.println("Asal:\tMamuju, Sulawesi Barat");

        // \" = menampilkan tanda kutip
        System.out.println("Saya suka bermain \"Badminton\".");

        // \\ = menampilkan tanda backslash
        System.out.println("Folder project saya ada di: C:\\Users\\Fuad\\Documents\\Java");
    }
}
